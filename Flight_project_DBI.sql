use Flight_ProJect
go
create table AirPorts(
	AirportId int Primary key,
	AirportCode char(10) not null,
	AirportName nvarchar(100) not null,
	Country Nvarchar(100) not null,
	City nvarchar(100) not null
)
Create table Flight_Routes(
	FlightRouteId int primary key,
	Departing_airport int foreign key references AirPorts(AirportID),
	Arriving_airport int foreign key references AirPorts(AirportID)
)
Create table Flight_Crews(
	FlightCrewId int primary key,
	FlightCrewCode char(10) not null
)
Create table Employees(
	EmployeeId int primary key,
	EmployeeName nvarchar(100) not null,
	Email nvarchar(100) not null,
	Gender char(1) not null check(Gender in ('M','F')),
	SuperVisor int foreign key references Employees(EmployeeId),
	[Role] nvarchar(100) not null,
	FlightCrewId int foreign key references Flight_Crews(FlightCrewId)
)
Create table Flights(
	FlightId int primary key,
	FlightCode char(10) not null,
	Departure_Time Datetime not null,
	Arrival_Time Datetime not null,
	AirplaneType nvarchar(100) not null,
	Airline nvarchar(100) not null,
	FlightRouteId int Foreign key references Flight_Routes(FlightRouteId),
	FlightCrewId int Foreign key references Flight_Crews(FlightCrewID),
	Departing_gate nvarchar(10) not null,
	Arriving_gate nvarchar(10) not null,

)
Create table Passengers(
	PassengerId int primary key,
	PasssengerName nvarchar(100) not null,
	DateOfBirth Date not null,
	Email nvarchar(100) not null,
	Gender char(1) not null check(Gender in ('M','F')),
	Nationality nvarchar(100) not null
)
Create table Tickets(
	TicketId int primary key,
	TicketCode char(10) not null,
	Seat nvarchar(50) not null,
	Class nvarchar(50) not null,
	PassengerId int foreign key references Passengers(PassengerId),
	FlightId int foreign key references Flights(FlightId),
	Price float not null
)


